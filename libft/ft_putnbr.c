#include "libft.h"

void	ft_putnbr(int n)
{
	int num;

	if (n < 0)
		ft_putchar('-');
	num = 1;
	if (n > 0)
		n = n * -1;
	while (n / num < -9)
		num = num * 10;
	while (num > 0)
	{
		ft_putchar(n / num % 10 * -1 + '0');
		num = num / 10;
	}
}
