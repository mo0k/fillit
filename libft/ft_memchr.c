#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	i = -1;
	while (++i < n)
		if (tmp[i] == (unsigned char)c)
			return ((void *)(tmp + i));
	return (NULL);
}
